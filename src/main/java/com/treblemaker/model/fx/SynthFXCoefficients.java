package com.treblemaker.model.fx;

public class SynthFXCoefficients {

    private int rating;
    private double sixteenthfreq;
    private double eigthfreq;
    private double quarterfreq;
    private double dottedquarterfreq;
    private double halffreq;
    private double fxvol;
    private double fxtype;
    private double fxmastervol;

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public double getSixteenthfreq() {
        return sixteenthfreq;
    }

    public void setSixteenthfreq(double sixteenthfreq) {
        this.sixteenthfreq = sixteenthfreq;
    }

    public double getEigthfreq() {
        return eigthfreq;
    }

    public void setEigthfreq(double eigthfreq) {
        this.eigthfreq = eigthfreq;
    }

    public double getQuarterfreq() {
        return quarterfreq;
    }

    public void setQuarterfreq(double quarterfreq) {
        this.quarterfreq = quarterfreq;
    }

    public double getDottedquarterfreq() {
        return dottedquarterfreq;
    }

    public void setDottedquarterfreq(double dottedquarterfreq) {
        this.dottedquarterfreq = dottedquarterfreq;
    }

    public double getHalffreq() {
        return halffreq;
    }

    public void setHalffreq(double halffreq) {
        this.halffreq = halffreq;
    }

    public double getFxvol() {
        return fxvol;
    }

    public void setFxvol(double fxvol) {
        this.fxvol = fxvol;
    }

    public double getFxtype() {
        return fxtype;
    }

    public void setFxtype(double fxtype) {
        this.fxtype = fxtype;
    }

    public double getFxmastervol() {
        return fxmastervol;
    }

    public void setFxmastervol(double fxmastervol) {
        this.fxmastervol = fxmastervol;
    }
}
